def generate_extended_dataset(base_commands, target_lines=5000):
    """
    Genera un dataset extendido de comandos Linux con variaciones
    """
    dataset = []
    dataset.append(['comando', 'descripcion_variacion'])  # Header
    
    # Calcular cuántas variaciones necesitamos por comando
    total_commands = len(base_commands)
    variations_per_command = target_lines // total_commands
    
    for command, variations in base_commands.items():
        # Asegurar que tenemos suficientes variaciones
        extended_variations = variations.copy()
        
        # Si necesitamos más variaciones, las generamos
        while len(extended_variations) < variations_per_command:
            # Seleccionar una variación base y crear una nueva basada en ella
            base_var = random.choice(variations)
            extended_variations.append(base_var)
        
        # Tomar solo las variaciones que necesitamos
        for i in range(variations_per_command):
            if i < len(extended_variations):
                dataset.append([command, extended_variations[i]])
    
    return dataset

def save_to_csv(dataset, filename='comandos_linux_extendido.csv'):
    """
    Guarda el dataset en un archivo CSV
    """
    with open(filename, 'w', newline='', encoding='utf-8') as file:
        writer = csv.writer(file)
        writer.writerows(dataset)
    
    print(f"Dataset guardado en {filename}")
    print(f"Total de líneas: {len(dataset)}")
